-- List of all employees and their assigned office location
-- This provides a reference view of the organizational structure
SELECT 
    t1.employeeNumber, 
    t1.reportsTo, 
    t1.jobTitle, 
    t1.firstName, 
    t1.lastName, 
    t1.officeCode, 
    t2.city, 
    t2.country 
FROM employees t1

-- Join to enrich employee data with office location
INNER JOIN offices t2
    ON t1.officeCode = t2.officeCode;

